async function exportDOCX_RETIFICADO(packageType, lang) {
    const startTime = Date.now();
    forensicLog('info', 'ENRICHMENT', '📝 Iniciando exportação de DOCX...', { packageType, lang });

    try {
        const _lang    = lang        || window.currentLang || 'pt';
        const _package = packageType || 'analyst';
        const t        = function (key) { return window.getTranslation(key, _lang); };

        const docxLanguage = {
            title:              t('docx_title'),
            subtitle:           t('docx_subtitle'),
            section_i:          t('docx_section_i_introduction'),
            section_ii:         t('docx_section_ii_findings'),
            section_iii:        t('docx_section_iii_custody'),
            section_iv:         t('docx_section_iv_conclusion'),
            section_v:          t('docx_section_v_legal'),
            footer_confidential:t('docx_footer_confidential'),
            footer_author:      t('docx_footer_author')
        };

        forensicLog('info', 'ENRICHMENT', '🌍 DOCX Language Context: ' + Object.keys(docxLanguage).length + ' chaves carregadas');

        if (typeof docx === 'undefined') {
            forensicLog('warn', 'ENRICHMENT', '⚠️ docx.js indisponível — activando motor DOCX nativo MIME (OOXML estruturado)');
            return new Promise(function(resolve, reject) {
                const existingScript = document.querySelector('script[src*="docx"]');
                if (!existingScript) {
                    const script = document.createElement('script');
                    script.src = 'https://cdnjs.cloudflare.com/ajax/libs/docx/8.5.0/docx.umd.min.js';
                    script.onload = function() {
                        forensicLog('info', 'ENRICHMENT', '✅ docx.js carregado dinamicamente via CDN');
                        resolve(exportDOCX_RETIFICADO(_package, _lang));
                    };
                    script.onerror = function() {
                        forensicLog('warn', 'ENRICHMENT', '⚠️ CDN docx.js inacessível — gerando DOCX via MIME-type Word nativo');
                        resolve(_exportDocxMimeNativo(_package, _lang, docxLanguage, UNIFEDSys, analysis, t));
                    };
                    document.head.appendChild(script);
                } else {
                    resolve(_exportDocxMimeNativo(_package, _lang, docxLanguage, UNIFEDSys, analysis, t));
                }
            });
        }

        const UNIFEDSys = window.UNIFEDSystem || {};
        const analysis  = UNIFEDSys.analysis  || {};

        const doc = new docx.Document({
            sections: [{
                properties: {},
                children: [
                    new docx.Paragraph({
                        text:      docxLanguage.title,
                        style:     'heading1',
                        alignment: docx.AlignmentType.CENTER,
                        spacing:   { after: 200 }
                    }),
                    new docx.Paragraph({
                        text:      docxLanguage.subtitle,
                        alignment: docx.AlignmentType.CENTER,
                        spacing:   { after: 400 }
                    }),
                    new docx.Table({
                        width: { size: 100, type: docx.WidthType.PERCENTAGE },
                        rows: [
                            new docx.TableRow({
                                cells: [
                                    new docx.TableCell({ children: [new docx.Paragraph(t('pdf_label_case_no'))] }),
                                    new docx.TableCell({ children: [new docx.Paragraph(UNIFEDSys.caseNumber || 'N/A')] })
                                ]
                            }),
                            new docx.TableRow({
                                cells: [
                                    new docx.TableCell({ children: [new docx.Paragraph(t('pdf_label_fiscal_year'))] }),
                                    new docx.TableCell({
                                        children: [new docx.Paragraph(
                                            String(UNIFEDSys.selectedYear || new Date().getFullYear())
                                        )]
                                    })
                                ]
                            })
                        ]
                    }),
                    new docx.Paragraph({ text: docxLanguage.section_i,   style: 'heading2', spacing: { before: 400, after: 200 } }),
                    new docx.Paragraph({ text: 'Introdução da análise forense...', spacing: { after: 200 } }),
                    new docx.Paragraph({ text: docxLanguage.section_ii,  style: 'heading2', spacing: { before: 400, after: 200 } }),
                    ...(_package === 'analyst'
                        ? [new docx.Paragraph({
                            text: 'BTOR: ' + window.formatForensicCurrency(analysis.btor || 0) +
                                  ' | BTF: ' + window.formatForensicCurrency(analysis.btf || 0),
                            spacing: { after: 200 }
                          })]
                        : [new docx.Paragraph({
                            text:    UNIFEDSys.legalNarrative || 'Narrativa jurídica não disponível',
                            spacing: { after: 200 }
                          })]),
                    new docx.Paragraph({ text: docxLanguage.section_iii, style: 'heading2', spacing: { before: 400, after: 200 } }),
                    new docx.Paragraph({
                        text: 'Master Hash: ' +
                              ((window.UNIFED_FORENSIC_SYSTEM &&
                                window.UNIFED_FORENSIC_SYSTEM.chainOfCustody &&
                                window.UNIFED_FORENSIC_SYSTEM.chainOfCustody.masterHash)
                                  ? window.UNIFED_FORENSIC_SYSTEM.chainOfCustody.masterHash.substring(0, 32)
                                  : 'N/A') + '...',
                        spacing: { after: 200 }
                    }),
                    new docx.Paragraph({ text: docxLanguage.section_iv,  style: 'heading2', spacing: { before: 400, after: 200 } }),
                    new docx.Paragraph({
                        text: _package === 'analyst'
                            ? t('analyst_verdict_compliant')
                            : t('lawyer_verdict_full_vindication'),
                        spacing: { after: 400 }
                    }),
                    new docx.Paragraph({ text: docxLanguage.footer_confidential, style: 'footer', spacing: { before: 200 } }),
                    new docx.Paragraph({ text: docxLanguage.footer_author,       style: 'footer' })
                ]
            }]
        });

        const fileName = 'UNIFED_Parecer_' + _package + '_' + _lang + '_' + Date.now() + '.docx';

        await docx.Packer.toBlob(doc).then(function (blob) {
            const url = window.URL.createObjectURL(blob);
            const a   = document.createElement('a');
            a.href    = url;
            a.download = fileName;
            document.body.appendChild(a);
            a.click();
            document.body.removeChild(a);
            window.URL.revokeObjectURL(url);
        });

        forensicLog('info', 'ENRICHMENT', '✅ DOCX exportado: ' + fileName, { durationMs: Date.now() - startTime });

        if (window.UNIFED_FORENSIC_SYSTEM && window.UNIFED_FORENSIC_SYSTEM.chainOfCustody) {
            await window.UNIFED_FORENSIC_SYSTEM.chainOfCustody.addEntry(
                'DOCX_EXPORTED',
                { fileName: fileName, packageType: _package, language: _lang },
                { format: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document' }
            );
        }

        return fileName;

    } catch (error) {
        forensicLog('error', 'ENRICHMENT', '❌ Erro ao exportar DOCX:', { message: error.message, stack: error.stack });
        alert('Erro ao exportar DOCX: ' + error.message);
        return null;
    }
}

